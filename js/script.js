function aumentaBraco(){
    let valor = document.getElementById("braco");
    if (valor.value < 10 && valor.value >=0){
        valor.value = parseInt(valor.value) + 1;
    }
}
function diminuiBraco(){
    let valor = document.getElementById("braco");
    if (valor.value <= 10 && valor.value >0){
        valor.value = parseInt(valor.value) - 1;
    }
}
